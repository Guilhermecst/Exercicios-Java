package br.com.cap5;

/*
 * O programa seguinte demonstra o uso do método charAt(), que apresentará o caractere
 * da 16ª posição do String "Programação com Java", contando a partir da posição de 
 * índice 0.
 */

public class C05EX02 {

	public static void main(String[] args) {
		
		String TEXTO = "Programação com Java";
		
		System.out.println();
		System.out.println(TEXTO.charAt(16));
		
	}

}
