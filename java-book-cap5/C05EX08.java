package br.com.cap5;

/*
 * O programa seguinte demontra o uso dos métodos toLowerCase() e toUpperCase() para a 
 * formatação respectiva do string em caracteres minúsculos e caracteres maiúsculos.
 */

public class C05EX08 {

	public static void main(String[] args) {
		
		String TEXTO = "Programação com Java";
		
		System.out.println(TEXTO.toLowerCase());
		System.out.println(TEXTO.toUpperCase());

	}

}
