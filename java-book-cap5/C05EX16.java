package br.com.cap5;

/*
 * O exemplo a seguir demonstra a conversão de um caractere da tabela ASCII em seu valor 
 * ASCII inteiro.
 */

public class C05EX16 {

	public static void main(String[] args) {
		
		char CARACTERE = 'A';
		int INTEIRO;
		
		INTEIRO = (int)CARACTERE;
		
		
		System.out.println("Valor = " + INTEIRO);
	}

}

// char para integer