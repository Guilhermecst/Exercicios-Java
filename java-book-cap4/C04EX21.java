package br.com.cap4;

/*
 * O programa a seguir é uma versão do programa C04EX17.java, porém dessa vez utilizaremos
 * a intrução do... while.
 */

public class C04EX21 {

	public static void main(String args[]) {
		
		int I = 1;
		
		System.out.println();
		
		do {
			System.out.println("Valor = " + I);
			I++;
		}
		while (I <= 5);
		
	}

}
