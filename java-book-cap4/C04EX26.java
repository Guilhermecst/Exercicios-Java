package br.com.cap4;

/* 
 * O programa a seguir exemplifica o uso de casting explícito.
 */

public class C04EX26 {

	public static void main(String args[]) {
		
		double A = 10.55;
		int B = (int) A;
		
		System.out.println();
		
		System.out.println("A = " + A);
		System.out.println("B = " + B);
		
	}

}
