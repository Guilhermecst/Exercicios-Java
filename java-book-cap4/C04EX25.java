package br.com.cap4;

/* 
 * O programa a seguir exemplifica o uso de casting implícito.
 */

public class C04EX25 {

	public static void main(String args[]) {
		
		long A = 10;
		float B = 7L;
		double C = 5.3f;
		
		System.out.println();
		
		System.out.println("A = " + A);
		System.out.println("B = " + B);
		System.out.println("C = " + C);
		
	}

}